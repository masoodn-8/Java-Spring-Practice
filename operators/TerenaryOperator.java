
public class TerenaryOperator {
    public static void main(String[] args) {

        /*
         * A shorthand for if-else:
         * 
         * condition ? value1 : value2
         */

        int a = 10, b = 5;
        int max = (a > b) ? a : b;
        System.out.println(max); // Output: 10

        
    }
}
